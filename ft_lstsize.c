#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p1;
	int		i;

	if (!lst)
		return (0);
	p1 = lst;
	i = 0;
	while (p1)
	{
		p1 = p1->next;
		i++;
	}
	return (i);
}
